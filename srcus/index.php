              <div class="acc_content">
				  
				  <p align="center"><img src="imgs/SRCUS_AnniversaryLogo.png" alt="SRCU Anniversary" width="292" height="224" class="img-responsive"></p>
				  
				  <p>The mission of the Southeast CUNA Management School (SRCUS) is to provide students the opportunity to develop their leadership capabilities so that they can contribute to the success of their credit unions. The school utilizes faculty from the University of Georgia’s Terry College of Business and experts from the credit union industry. The curriculum is designed to be delivered over a three-year period with students in residence for course work each summer for eight days. In between the summers, students apply the knowledge gained from the School to projects that require strategic analysis of their credit unions and research of relevant issues facing the credit union industry.</p>
			
			<p>The annual SRCUS Scholarship, awarded by LSCU on the basis of future potential, involvement in the credit union movement, and other criteria, exists to encourage credit union personnel to become more informed, competent employees through attendance at the School. Scholarship proceeds are used towards tuition for the current year; recipients must re-apply each year.</p>
			
			<p><em>SRCUS scholarships are available to professionals from LSCU-affiliated credit unions. Funding for this scholarship program is provided by the Southeast Regional Credit Union Schools and the LSCU.</em></p>
			
			<p>Scholarship applications to 2022 <strong>Southeast Regional Credit Union Schools (SRCUS)</strong> were due by April 29, 2022. Scholarships have been announced and awarded to credit unions.</p>
			<p>The Southeastern Credit Union Foundation (SECUF) awards <a href="https://www.lscu.coop/foundation/scholarships.php" target="_blank">professional development grants</a> throughout the year for credit union CEOs, employees, board members and supervisory committee members to attend the League of Southeastern Credit Unions &amp; Affiliates (LSCU) and Credit Union National Association (CUNA) educational events.</p>
			<p>The professional development grants program is designed for smaller asset size credit unions and credit unions with financial hardships. The grants not only cover tuition or registration fee but may also cover a portion of travel costs. Professional development grants are awarded to individuals who best exemplify the credit union philosophy, and whose attendane at approved programs would be beneficial in the prepetuation of credit union goals and ideas.</p>
			<p>Professional development grant requests must be received at least 30 days before the event. Credit unions receiving SAS (Small Asset Size) funds must exhaust those funds before they are able to receive professional development grant awards.</p>
			
